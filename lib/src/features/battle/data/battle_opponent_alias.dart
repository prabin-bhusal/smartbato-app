String pickSystemOpponentAlias({int? battleId}) {
  if (battleOpponentAliases.isEmpty) {
    return 'Opponent';
  }

  final seed = battleId != null && battleId > 0
      ? battleId
      : DateTime.now().millisecondsSinceEpoch;
  final random = Random(seed);
  return battleOpponentAliases[random.nextInt(battleOpponentAliases.length)];
}
